export type PlacementEdgeX = 'left' | 'right' | 'start' | 'end'
export type PlacementEdgeY = 'top' | 'bottom'
export type PlacementEdge = PlacementEdgeX | PlacementEdgeY
export type PlacementX = PlacementEdgeX | 'center'
export type PlacementY = PlacementEdgeY | 'center'
export type Placement = PlacementX | PlacementY